#Operatoren und Operanden
'''
+       Plus
+=      Plus und Zuweisung
-       Minus  
-=      Minus und Zuweisung
/       Geteilt
*       Mal
=       Zuweisung
<       Kleiner
>       Groesser
<=      Kleinergleich
>=      Groessergleich
==      Vergleichsop
'''

#Variablen
a = 2
b = 10

#Plus
a + b

#Plus und Zuweisung
a += b #(a = a + b)

#Minus
a - b

#Minus und Zuweisung
a -= b #(a = a - b)

#Geteilt
a / b

#Mal
a * b

#Zuweisung
a = b

#Kleiner
a < b

a = 2
b = 10

while a < b:
    print(a)
    a += 1

#Groesser
a > b

#Kleinergleich
a <= b

#Groessergleich
a >= b

#Vergleichsop
a == b


